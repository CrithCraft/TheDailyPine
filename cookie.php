    <div class="text">
        <b>Cookie-файлы (Куки)</b> — небольшой фрагмент данных, отправленный веб-сервером и хранимый на цифровом устройстве пользователя - компьютере, ноутбуке, планшете, смартфоне и т.п. Веб-клиент (обычно веб-браузер) каждый раз при попытке открыть страницу соответствующего сайта пересылает этот фрагмент данных веб-серверу в составе HTTP-запроса. Cookie-файлы не позволяют идентифицировать личность.
        <br><br>
        <b>Cookie-файлы</b> используются в целях сбора статистических данных, которые позволяют определить, какие разделы сайта привлекают наибольшее внимание посетителей, а также в целях улучшения условий пользования сайтом посетителями. Пользователь может отказаться от использования Cookie-файлов, установив соответствующим образом настройки браузера согласно инструкциям, представленным ниже. Блокировка <b>cookie-файлов</b> может повлиять на функциональные возможности сайта.
